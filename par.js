// Verificar se um número é par ou ímpar
numero = 10

numero/2

// Operador que pega o resto -> módulo -> %
resto = numero % 2
// se(pergunta;resposta verdadeira; resposta falsa)
if (resto == 0){
    console.log('O numero é par')
}
else{
    console.log('O numero é impar')
}
